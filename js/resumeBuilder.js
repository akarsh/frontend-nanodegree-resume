// This function capitalizes the family name and first character of the first name
function inName(nameToConvert) {
  var convertName = nameToConvert.split(" ");
  convertName[0] = convertName[0].slice(0, 1).toUpperCase() + convertName[0].slice(1).toLowerCase();
  convertName[1] = convertName[1].toUpperCase();
  convertName = convertName.join(" ");
  return convertName;
}

